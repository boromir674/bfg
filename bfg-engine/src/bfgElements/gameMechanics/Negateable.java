package bfgElements.gameMechanics;

/**
 * A class implementing this interface allows for being negated.
 * @author kostadinos
 */
public interface Negateable {
	
	/**
	 * This method reports whether the object has been negated or not. 
	 * @return the boolean flag. True for negated object
	 */
	public boolean isNegated();
}
