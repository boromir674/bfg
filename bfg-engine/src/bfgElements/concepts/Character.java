package bfgElements.concepts;

public class Character {
	
	private String name;
	private String label;
	
	public Character(String name, String label) {
		this.name = name;
		this.label = label;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}
	
	
}
